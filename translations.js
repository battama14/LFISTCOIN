function updateLanguageButton(currentLang) {
  const langButton = document.getElementById('languageButton');
  if (langButton) {
    const nextLang = currentLang === 'fr' ? 'en' : 'fr';
    const nextLangText = translations[currentLang][`language_${nextLang}`];
    langButton.innerHTML = nextLangText;
    langButton.onclick = () => changeLanguage(nextLang);
  }
}

