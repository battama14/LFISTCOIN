// Fonction pour changer la langue
function changeLanguage(lang) {
  // Sauvegarder la préférence
  localStorage.setItem('lfist_language', lang);
  
  // Mettre à jour l'attribut lang du HTML
  document.documentElement.lang = lang;
  
  // Traduire tous les éléments avec data-translate
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[lang] && translations[lang][key]) {
      element.innerHTML = translations[lang][key];
    }
  });
  
  // Traduire les placeholders
  document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
    const key = element.getAttribute('data-translate-placeholder');
    if (translations[lang] && translations[lang][key]) {
      element.placeholder = translations[lang][key];
    }
  });
  
  // Mettre à jour le bouton de langue
  updateLanguageButton(lang);
}

// Fonction pour mettre à jour le bouton de langue
function updateLanguageButton(currentLang) {
  const langButton = document.getElementById('languageButton');
  if (langButton) {
    const nextLang = currentLang === 'fr' ? 'en' : 'fr';
    const nextLangText = translations[currentLang][`language_${nextLang}`];
    langButton.innerHTML = nextLangText;
    langButton.onclick = () => changeLanguage(nextLang);
  }
}

// Initialiser la langue au chargement de la page
function initLanguage() {
  const savedLang = localStorage.getItem('lfist_language') || 'fr';
  changeLanguage(savedLang);
}

// Détecter la langue du navigateur (optionnel)
function detectBrowserLanguage() {
  const browserLang = navigator.language || navigator.userLanguage;
  if (browserLang.startsWith('en')) {
    return 'en';
  }
  return 'fr'; // Par défaut français
}

// Initialiser au chargement de la page
document.addEventListener('DOMContentLoaded', initLanguage);

// Exporter pour utilisation globale
window.changeLanguage = changeLanguage;
window.initLanguage = initLanguage;